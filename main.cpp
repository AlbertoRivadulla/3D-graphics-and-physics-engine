    GLSandbox sandbox(SCR_WIDTH, SCR_HEIGHT, "Title");

    GLGeometry::GLQuad algo;
    algo.setModelMatrix(glm::vec3(0.,0.,0.), 0., glm::vec3(0.,0.,0.), glm::vec3(1.,1.,1.));

